num_players = ["Маша", "Петя", "Саша", "Оля", "Кирилл", "Коля"]

# Определяем середину списка для разделения
a = int(len(num_players)) // 2

# Создаем две команды с использованием слайсирования
team1 = num_players[:a]
team2 = num_players[a:]

# Печатаем каждую команду на отдельной строке
print(team1)
print( team2)